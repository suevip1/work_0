package com.dtstack.engine.dto;

import java.util.Date;

public class TimeUsedNode {
    private Date time;
    private String used;

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    public String getUsed() {
        return used;
    }

    public void setUsed(String used) {
        this.used = used;
    }
}
